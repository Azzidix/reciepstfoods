package com.example.foodmeal;

public class Author {
    private String nameAuthor, emailAuthor, igAuthor;
    private static int imageAuthor;

    public String getNameAuthor() {
        return nameAuthor;
    }

    public void setNameAuthor(String nameAuthor) {
        this.nameAuthor = nameAuthor;
    }

    public String getEmailAuthor() {
        return emailAuthor;
    }

    public void setEmailAuthor(String emailAuthor) {
        this.emailAuthor = emailAuthor;
    }

    public String getIgAuthor() {
        return igAuthor;
    }

    public void setIgAuthor(String igAuthor) {
        this.igAuthor = igAuthor;
    }

    public static int getImageAuthor() {
        return imageAuthor;
    }

    public void setImageAuthor(int imageAuthor) {
        this.imageAuthor = imageAuthor;
    }
}
